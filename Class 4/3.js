// Increment and Decrement Operators
let num = 10;
console.log("Original Value:", num); // 10

// Post-increment
console.log("Post-increment:", num++); // 10
console.log("After Post-increment:", num); // 11

// Pre-increment
console.log("Pre-increment:", ++num); // 12
console.log("After Pre-increment:", num); // 12

// Post-decrement
console.log("Post-decrement:", num--); // 12
console.log("After Post-decrement:", num); // 11